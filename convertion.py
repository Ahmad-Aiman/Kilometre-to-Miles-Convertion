#1 km = 1000 m
#1 km = 0.621 miles

Kilo_KM = float(input("Enter in Kilometre (KM): "))

Kilo_KM = float (Kilo_KM)

KilometretoMeter = Kilo_KM * 1000
KilometretoMiles = Kilo_KM * 0.621

print("Value in Metre: ", KilometretoMeter , "Metre")
print("Value in Miles: ", KilometretoMiles, "Miles")



